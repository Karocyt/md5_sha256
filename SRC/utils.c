#include "ft_ssl.h"

uint32_t	lr(uint32_t x, uint32_t c)
{
	return ((x << c) | (x >> (32 - c)));
}

uint32_t	rr(uint32_t x, uint32_t c)
{
	return ((x >> c) | (x << (32 - c)));
}
